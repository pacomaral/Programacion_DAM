
public class UsandoFor {

	public static void main(String[] args) {
		
		/**
		 * Estructura de control FOR
		 */
		
		for (int i=0; i<10; i++){
			System.out.println(i);		//Esto imprimiría del 0 al 9
		}
		
		System.out.println("-------------");
		
		for (int i=10; i>=0; i = i-2){
			System.out.println(i);		//Esto imprimiría del 10 al 0 decreciendo cada 2
 		}

	}

}
